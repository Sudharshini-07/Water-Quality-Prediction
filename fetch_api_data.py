# fetch_api_data.py

import requests
import pandas as pd

# API for real-time water quality (pH example from USGS)
url = "https://www.waterqualitydata.us/data/Result/search?countrycode=US&statecode=US:CA&characteristicName=PH&startDateLo=2024-07-01&mimeType=csv"

# Make the API call
response = requests.get(url)

# Save to CSV file
with open("realtime_water_data.csv", "wb") as f:
    f.write(response.content)

